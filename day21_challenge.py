op = input('Enter operator: ')
num1 =int(input("Enter 1st Number: "))
num2 =int(input("Enter 2nd Number: "))

if(op == "+"):
    print(num1,'+',num2,'=',(num1+num2))
if(op == "-"):
    print(num1,'-',num2,'=',(num1-num2))
if(op == "*"):
    print(num1,'*',num2,'=',(num1*num2))
if(op == "/"):
    print(num1,'/',num2,'=',(num1/num2))
